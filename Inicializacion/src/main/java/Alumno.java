public class Alumno {

    private String curso;
    private byte sesion;
    private String objetivo;
    public String getCurso() {
        return curso;
    }

    public byte getSesion() {
        return sesion;
    }

    public String getObjetivo() {
        return objetivo;
    }

        public Alumno()
        {
            this.curso=curso;
            this.objetivo=objetivo;
            this.sesion=sesion;
        }



}
